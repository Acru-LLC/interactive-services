      this.keyUpEventsDisable(e);

      if (e.keyCode == 40) {
        //down
      } else if (e.keyCode == 38) {
        //up
      } else if (e.keyCode == 37) {
        //left
      } else if (e.keyCode == 39) {
        //right
      }
    },
    focusVal(evt) {
      this.inputs.forEach((el) => {
        el.classList.remove("borderInput");
      });
      evt.target.classList.add("borderInput");
    },
  },
